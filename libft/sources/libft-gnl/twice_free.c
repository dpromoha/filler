#include "libft.h"

void	twice_free(char ***arr)
{
	int i;

	i = 0;
	if (!arr || !*arr)
		return ;
	else
	{
		while (arr[0][i])
			ft_strdel(&arr[0][i++]);
	}
	free(arr[0]);
	arr[0] = NULL;
}
